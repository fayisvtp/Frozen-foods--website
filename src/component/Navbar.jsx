import React, { useState } from 'react';
import {
  MDBNavbar,
  MDBContainer,
  MDBIcon,
  MDBNavbarNav,
  MDBNavbarItem,
  MDBNavbarLink,
  MDBNavbarToggler,
  MDBNavbarBrand,
  MDBCollapse
} from 'mdb-react-ui-kit';
import { Link } from 'react-router-dom';
import { useDispatch } from 'react-redux';
import { clearUserId, clearUserLogin, clearUserToken } from '../Redux/ItemSlice';
import toast from 'react-hot-toast';

export default function Navbar() {
  const [openNavColor, setOpenNavColor] = useState(false);
  const dispatch = useDispatch()
  const handlelogout = ()=>{
      dispatch(clearUserToken())
      dispatch(clearUserId())
      dispatch(clearUserLogin())
      toast.error ("logout")
  }

  return (
    <>
      <MDBNavbar expand='lg' dark bgColor='primary'>
        <MDBContainer fluid>
          <Link to={"/"}>
          <MDBNavbarBrand >Frosty</MDBNavbarBrand>
          </Link>
          <MDBNavbarToggler
            type='button'
            data-target='#navbarColor02'
            aria-controls='navbarColor02'
            aria-expanded='false'
            aria-label='Toggle navigation'
            onClick={() => setOpenNavColor(!openNavColor)}
          >
            <MDBIcon icon='bars' fas />
          </MDBNavbarToggler>
          <MDBCollapse open={openNavColor} navbar>
            <MDBNavbarNav className='me-auto mb-2 mb-lg-0'>
              <MDBNavbarItem className='active'>
             
              </MDBNavbarItem>
              <MDBNavbarItem>
                <Link to={"/userlogin"}>
                                <MDBNavbarLink>Login</MDBNavbarLink>
                                </Link>

              </MDBNavbarItem>
              <MDBNavbarItem>
             
                                <MDBNavbarLink onClick={handlelogout}>LogOut</MDBNavbarLink>
                              

              </MDBNavbarItem>
              <MDBNavbarItem>
                <Link to={"/cart"}>
                <MDBNavbarLink>cart</MDBNavbarLink>
                </Link>
              </MDBNavbarItem>
              <MDBNavbarItem>
                <Link to={"/wishlist"}>
                <MDBNavbarLink>wishliist</MDBNavbarLink>
                </Link>
              </MDBNavbarItem>
            </MDBNavbarNav>
          </MDBCollapse>
        </MDBContainer>
      </MDBNavbar>

     

     
    </>
  );
}

